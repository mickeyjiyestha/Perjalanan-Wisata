    private void showPaketData() {
    try {
        Statement stmt = Con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT KodePaket, RutePerjalanan, MinimumPeserta, Tarif FROM tbl_paket");

        // Membersihkan tabel sebelum menambahkan data baru
        model.setRowCount(0);

        while (rs.next()) {
            String kodePaket = rs.getString("KodePaket");
            String rutePerjalanan = rs.getString("RutePerjalanan");
            String minimumPeserta = rs.getString("MinimumPeserta");
            String tarif = rs.getString("Tarif");

            // Menambahkan data ke dalam tabel
            model.addRow(new Object[]{kodePaket, rutePerjalanan, minimumPeserta, tarif});
        }

        stmt.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}
    
