    private void KirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KirimActionPerformed
                                      
    String kodePaketInput = jTextField1.getText().trim();
    String jumlahPeserta = jTextField2.getText().trim();
    String rutePerjalanan = "";
    int biayaTotal = 0;
    
    // Validasi jika kode paket atau minimum peserta kosong
    if (kodePaketInput.isEmpty() || jumlahPeserta.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Kode Paket atau Minimum Peserta Kosong", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Menghentikan proses selanjutnya jika kode paket atau minimum peserta kosong
    }
    
    if (!(kodePaketInput.equals("1") || kodePaketInput.equals("2") || kodePaketInput.equals("3"))) {
        // Kode paket tidak valid
        JOptionPane.showMessageDialog(this, "Kode Paket Tidak Valid", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Menghentikan proses selanjutnya jika kode paket tidak valid
    }
    
    // Validasi jumlah peserta
    int minimumPeserta = 0;
    if (kodePaketInput.equals("1")) {
        minimumPeserta = 8;
    } else if (kodePaketInput.equals("2")) {
        minimumPeserta = 6;
    } else if (kodePaketInput.equals("3")) {
        minimumPeserta = 4;
    }
    
    if (Integer.parseInt(jumlahPeserta) < minimumPeserta) {
        // Peserta tidak valid
        JOptionPane.showMessageDialog(this, "Peserta Tidak Valid (Minimum Peserta: " + minimumPeserta + ")", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Menghentikan proses selanjutnya jika jumlah peserta tidak valid
    }
    
    if (kodePaketInput.equals("1")) {
        rutePerjalanan = "Kode Paket = 1 || Pantai pandawa - Garuda Wisnu Kencana || Jumlah Peserta: " + jumlahPeserta;
        biayaTotal = Integer.parseInt(jumlahPeserta) * 150000;
    } else if (kodePaketInput.equals("2")) {
        rutePerjalanan = "Kode Paket = 2 || Tanah Lot - Desa Penglipuran - Danau Batur || Jumlah Peserta: " + jumlahPeserta;
        biayaTotal = Integer.parseInt(jumlahPeserta) * 500000;
    } else if (kodePaketInput.equals("3")) {
        rutePerjalanan = "Kode Paket = 3 || Monkey Forest - Pantai Kuta - Bali Safari - Tirta Empul || Jumlah Peserta: " + jumlahPeserta;
        biayaTotal = Integer.parseInt(jumlahPeserta) * 1000000;
    } else {
        rutePerjalanan = "Kode Paket Tidak Ditemukan";
    }
    
    
    jTextField3.setText(rutePerjalanan + " || Biaya Total: " + biayaTotal);

    }//GEN-LAST:event_KirimActionPerformed

